# - gpt is basically an LLM
# - LLM stands for large language model
# - LLMs give us output based on how they have been trained
# - an LLM understands and generates human language
# - OpenAI has actually trained these models on vast amount of Internet data
# - at its core, an LLM is a sophisticated type of AI that has been trained to understand, process and generate human language
# - whenever we talk to systems, we always have to talk in a structured manner
# - but using these LLMs, we can actually talk to the machine in a natural language like we humans do, this is the main USP of these LLMs